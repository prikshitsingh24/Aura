

pub enum Shapes {
    Pyramid
}



impl Shapes {
    pub fn pyramid() -> Vec<f32> {
        return vec![
            // Base (2 triangles)
            -1.0, 0.0,  1.0,   // B
            1.0, 0.0,  1.0,   // C
            1.0, 0.0, -1.0,   // D

            1.0, 0.0, -1.0,   // D
            -1.0, 0.0, -1.0,   // E
            -1.0, 0.0,  1.0,   // B

            // Sides (4 triangles)
            0.0, 1.0,  0.0,   // A
            -1.0, 0.0,  1.0,   // B
            1.0, 0.0,  1.0,   // C

            0.0, 1.0,  0.0,   // A
            1.0, 0.0,  1.0,   // C
            1.0, 0.0, -1.0,   // D

            0.0, 1.0,  0.0,   // A
            1.0, 0.0, -1.0,   // D
            -1.0, 0.0, -1.0,   // E

            0.0, 1.0,  0.0,   // A
            -1.0, 0.0, -1.0,   // E
            -1.0, 0.0,  1.0,   // B
        ]
    }
}